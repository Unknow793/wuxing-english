# 构建词表
# ============================================================
result_words = []
seen = set()

def make_entry(word, cn, pos, category_cn):
    wl = word.lower()
    if wl in seen:
        return None
    seen.add(wl)

    if wl in OLD_MAP:
        old = OLD_MAP[wl]
        elem = old['element']
        sentence = old.get('sentence', '')
        if not sentence:
            sentence = make_sentence(word, cn, pos)
    else:
        elem = assign_element(word)
        sentence = make_sentence(word, cn, pos)

    return {
        'word': word,
        'cn': cn,
        'pos': pos,
        'category_cn': category_cn,
        'element': elem,
        'sentence': sentence,
        'tier': TIER_MAP.get(category_cn, 'intermediate')
    }

# Step 1: 先从旧词库映射已有词
for w in OLD['words']:
    wl = w['word'].lower()
    old_cat = w.get('category_cn', '其他')
    new_cat = CAT_MAP.get(old_cat, '其他补充')
    entry = make_entry(w['word'], w['cn'], w['pos'], new_cat)
    if entry:
        result_words.append(entry)

# Step 2: 再加新词（不存在的才加）
for nw in NEW_WORDS:
    entry = make_entry(*nw)
    if entry:
        result_words.append(entry)

