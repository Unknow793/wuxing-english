def make_sentence(word, cn, pos):
    w = word.lower()
    if w in OLD_MAP and OLD_MAP[w].get('sentence'):
        return OLD_MAP[w]['sentence']

    concrete_nouns = ['knee','shoulder','plate','bowl','cup','fork','spoon','knife',
        'wall','floor','mirror','shelf','drawer','closet','computer','screen',
        'keyboard','mouse','lantern','robot','crayon','boot','taxi','headache',
        'toothache','bathroom','bedroom','library','hospital','supermarket',
        'restaurant','museum','bridge','farm','village','building','gym','pool',
        'monday','tuesday','wednesday','thursday','friday','saturday','sunday',
        'calendar','clock','drum','guitar','piano','violin','doll','ball','block',
        'kite','balloon']
    if pos == 'noun' and w.lower() in concrete_nouns:
        return f'I see a {w.lower()}.'
    elif pos == 'noun':
        return f'This is {w.lower()}.'
    elif pos == 'verb':
        es_words = {'wash','brush','fix','mix','pass','push','reach','teach','dress','watch','catch','touch'}
        ie_words = {'fly','try','cry','carry','hurry','study','worry'}
        if w in es_words:
            return f'He {w}es every day.'
        elif w in ie_words:
            return f'She {w[:-1]}ies well.'
        else:
            return f'I can {w}.'
    elif pos == 'adj':
        return f'I feel {w}.'
    elif pos == 'adv':
        return f'Do it {w}.'
    elif pos == 'pron':
        return f'{word.capitalize()} is here.'
    elif pos == 'prep':
        return f'Go {w} the door.'
    elif pos == 'conj':
        return f'Come {w} you can.'
    elif pos == 'art':
        return f'{word.capitalize()} one is good.'
    elif pos == 'num':
        return f'{word.capitalize()} is a number.'
    elif pos == 'interj':
        return f'{word.capitalize()}!'
    elif pos == 'aux':
        return f'I {w} go.'
    return f'This is {w}.'

