# ============================================================
by_tier = {}
by_cat = {}
elem_count = {}
for w in result_words:
    by_tier[w['tier']] = by_tier.get(w['tier'], 0) + 1
    by_cat[w['category_cn']] = by_cat.get(w['category_cn'], 0) + 1
    elem_count[w['element']] = elem_count.get(w['element'], 0) + 1

print(f'五行英语牌 — 词表生成报告 v2.0')
print(f'=' * 50)
